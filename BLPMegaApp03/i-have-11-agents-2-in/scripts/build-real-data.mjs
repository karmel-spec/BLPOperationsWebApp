import fs from "node:fs";
import path from "node:path";

const root = path.resolve(import.meta.dirname, "..");
const configPath = path.join(root, "data", "source-config.json");
const outPath = path.join(root, "real-data.js");
const config = JSON.parse(fs.readFileSync(configPath, "utf8"));

function exists(p) {
  return Boolean(p && fs.existsSync(p));
}

function walkFiles(dir, maxFiles = 400) {
  if (!exists(dir)) return [];
  const out = [];
  const stack = [dir];
  while (stack.length && out.length < maxFiles) {
    const current = stack.pop();
    for (const entry of fs.readdirSync(current, { withFileTypes: true })) {
      if (entry.name.startsWith(".")) continue;
      const full = path.join(current, entry.name);
      if (entry.isDirectory()) stack.push(full);
      else out.push(full);
      if (out.length >= maxFiles) break;
    }
  }
  return out;
}

function fileSummary(file) {
  const stat = fs.statSync(file);
  return {
    name: path.basename(file),
    path: file,
    ext: path.extname(file).replace(".", "").toLowerCase() || "file",
    size: stat.size,
    modifiedAt: stat.mtime.toISOString(),
  };
}

const avatarFolder = config.sources.avatarFolder.localPath;
const avatarFiles = walkFiles(avatarFolder)
  .filter((file) => /\.(png|jpe?g|webp)$/i.test(file))
  .map(fileSummary)
  .sort((a, b) => a.name.localeCompare(b.name));

const driveVault = config.sources.googleDriveKnowledgeVault;
const vaultPath = driveVault.localPath;
const vaultFiles = walkFiles(vaultPath)
  .filter((file) => /\.(md|txt|pdf|docx?|xlsx?|csv|json)$/i.test(file))
  .map(fileSummary)
  .sort((a, b) => a.name.localeCompare(b.name));

const orgFiles = (config.sources.orgCharts.files || [])
  .filter(exists)
  .map(fileSummary)
  .sort((a, b) => a.name.localeCompare(b.name));

const realData = {
  generatedAt: new Date().toISOString(),
  sources: config.sources,
  avatarInventory: {
    status: exists(avatarFolder) ? "connected" : "missing",
    localPath: avatarFolder,
    count: avatarFiles.length,
    files: avatarFiles,
  },
  knowledgeVault: {
    status: exists(vaultPath) ? "local-sync-connected" : "drive-url-only",
    url: driveVault.url,
    localPath: vaultPath,
    count: vaultFiles.length,
    files: vaultFiles.slice(0, 80),
  },
  orgCharts: {
    status: orgFiles.length ? "connected" : "missing",
    count: orgFiles.length,
    files: orgFiles,
  },
};

fs.writeFileSync(
  outPath,
  `// Generated by scripts/build-real-data.mjs. Do not hand-edit.\nwindow.BLP_REAL_DATA = ${JSON.stringify(realData, null, 2)};\n`,
);

